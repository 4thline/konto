package org.fourthline.konto.shared.entity;

import org.fourthline.konto.shared.Constants;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;
import java.io.Serializable;

@Entity
@Table(name = "ACCOUNT_GROUP")
public class AccountGroup implements Serializable {

    @Id
    @GeneratedValue(generator = Constants.SEQUENCE_NAME)
    @Column(name = "ACCOUNT_GROUP_ID")
    private Long id;

    @Column(name = "GROUP_NAME", nullable = false, length = 100)
    private String name = "My Group";

    public AccountGroup() {
    }

    public AccountGroup(String name) {
        this.name = name;
    }

    public AccountGroup(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return getName();
    }
}
